# FOR
for i in range(1, 101):
    if i % 15 == 0:
        print("Dingdong")
    elif i % 3 == 0:
        print("Ding")
    elif i % 5 == 0:
        print("Dong")
    else:
        print(i)

# WHILE
i = 1
while i <= 100:
    if i % 15 == 0:
        print("Dingdong")
    elif i % 3 == 0:
        print("Ding")
    elif i % 5 == 0:
        print("Dong")
    else:
        print(i)
    i += 1
